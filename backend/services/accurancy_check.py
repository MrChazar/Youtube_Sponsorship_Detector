import statistics
a = ["1590.62,1651.70 | 1590,1652",
"955.88,1013.68 | 955,1015",
"329.96,406.36 | 328,350",
"879.18,1005.0 | 879,1006",
"95.84,166.04 | 95,166",
"39.42,98.83 | 39,70",
"51.96,54.52 | 49,124",
"64.52,138.34 | 65,140",
"772.34,830.12 |732,830",
"458.08,497.38 | 452,498",]
acc = []
for el in a:
    b = el.split("|")
    b0 = b[0].split(',')
    b1 = b[1].split(',')
    b = [float(b0[0]),float(b0[1])]
    c = [float(b1[0]), float(b1[1])]

    acc.append(
        (min(b[1], c[1])-max(b[0], c[0])-0.25*((c[0]-b[0])+(b[1]-c[1])))/max(1, c[1]-c[0]))
print(acc)
for el in acc:
    if el > 10:
        acc.remove(el)
print(statistics.mean(acc))